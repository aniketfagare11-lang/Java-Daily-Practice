package Day06;

public class WhileLoop {
    public static void main (String [] args)
    {
        int i=1;
        while(i<=3) //While Loop in Java...
        {
            System.out.println(i);
            i++;
        }

        // Question -- Print 100 to 200 Natural Numbers..
        int a=100;
        System.out.println("Printing 100 to 200 Natural Numbers:");
        while(a<=200) 
        {
            System.out.println(a);
            a++;
        }   
                   
    }
    
}

    

